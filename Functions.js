// Function Declration

function greet(name) {
    return "Hello, " + name + "!";
}



// Function Expression

const greet = function(name) {
    return "Hello, " + name + "!";
};

// Arrow Function

const greet = (name) => {
    return "Hello, " + name + "!";
};

// Arrow functions are often used for concise one-liners:

const greet = (name) => "Hello, " + name + "!";


// Named Functions

const greet = function greetFunction(name) {
    return "Hello, " + name + "!";
};

